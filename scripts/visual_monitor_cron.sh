#!/bin/bash
# =============================================================================
# 紫微制造 - 视觉监控定时任务
# 功能：每 8 分钟自动截取 Dashboard，视觉化监控
# =============================================================================

SCRIPT_DIR="/home/admin/Ziwei/scripts"
PYTHON="/usr/bin/python3"

echo "=========================================="
echo "👁️ 紫微视觉监控任务"
echo "=========================================="
echo "执行时间：$(date '+%Y-%m-%d %H:%M:%S')"
echo ""

# 执行视觉观察
$PYTHON $SCRIPT_DIR/visual_observer.py observe

# 清理 30 天前的截图（每天执行一次）
if [ $(date +%H) -eq 02 ] && [ $(date +%M) -eq 00 ]; then
    echo ""
    echo "🧹 执行清理任务..."
    $PYTHON $SCRIPT_DIR/visual_observer.py cleanup 30
fi

echo ""
echo "=========================================="
echo "✅ 视觉监控任务完成"
echo "=========================================="
